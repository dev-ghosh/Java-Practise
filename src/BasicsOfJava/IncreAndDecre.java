package BasicsOfJava;

public class IncreAndDecre {
    public static void main(String[] args) {
        int x=5;
        System.out.println(x);
        x++; //x++ or x=x+1 same thing
        System.out.println(x);
        x=6;
        System.out.println(x);
        x--; //x-- or x=x-1 same thing
        System.out.println(x);
    }
}
